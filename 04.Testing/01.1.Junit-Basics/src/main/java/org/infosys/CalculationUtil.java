package org.infosys;

public class CalculationUtil {
    public int add(int a, int b)
    {
        return a+b;
    }

    public int multiply(int a, int b)
    {
        return a*b;
    }

    public String parseString(String str)
    {
        return str;
    }

    public int divide(int a, int b)
    {
        return a/b;
    }
}
